    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Gestore gestore = new Gestore();
        try {
            boolean ok = gestore.deleteAllCredenziali();
            if (ok) this.jLabel7.setText("Credenziali eliminate correttamente");
        } catch (FileNotFoundException ex) {
            this.jLabel7.setText("Errore");
        } catch (IOException ex) {
            Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String username = jTextPane1.getText();
        String password = jTextPane2.getText();
        
        Login credenziali;
        try {
            credenziali = new Login(username, password);
            Gestore gestore = new Gestore();
            boolean ok = gestore.addCredenziali(credenziali);
            if (ok) jLabel7.setText("Nuove credenziali aggiunte correttamente");
            else jLabel7.setText("Lo username esiste gi√† nel file!");
        } catch (Exception ex) {
            jLabel7.setText("errore");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

