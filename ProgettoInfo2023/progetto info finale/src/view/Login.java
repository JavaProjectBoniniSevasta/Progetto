    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String materia = jTextField4.getText();
        Materia m = new Materia (materia);
        try {
            float media = registro.media(m);
            if (media == -999) this.jLabel3.setText("Materia non presente");
            else this.jLabel3.setText("La media di questa materia è: " + media);
        } catch (Exception ex) {
            this.jLabel3.setText("errore");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

