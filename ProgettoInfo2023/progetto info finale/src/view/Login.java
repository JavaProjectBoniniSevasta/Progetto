    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String m = jTextField2.getText();
        Materia materia = new Materia(m);
        String v = jTextField3.getText();
        float voto = Float.parseFloat(v);
        try {
            boolean ok = registro.addVoto(materia, voto);
            if (ok) jLabel5.setText("Voto aggiunto");
            else jLabel5.setText("Materia non presente");
        } catch (Exception ex) {
            jLabel5.setText("Errore");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

